package com.accp.domain;

public class Workcard {
    private Integer wodid;

    private String wodlicense;

    private String wodbrand;

    private String wodtype;

    private Integer wodmileage;

    private String temid;

    public Integer getWodid() {
        return wodid;
    }

    public void setWodid(Integer wodid) {
        this.wodid = wodid;
    }

    public String getWodlicense() {
        return wodlicense;
    }

    public void setWodlicense(String wodlicense) {
        this.wodlicense = wodlicense;
    }

    public String getWodbrand() {
        return wodbrand;
    }

    public void setWodbrand(String wodbrand) {
        this.wodbrand = wodbrand;
    }

    public String getWodtype() {
        return wodtype;
    }

    public void setWodtype(String wodtype) {
        this.wodtype = wodtype;
    }

    public Integer getWodmileage() {
        return wodmileage;
    }

    public void setWodmileage(Integer wodmileage) {
        this.wodmileage = wodmileage;
    }

    public String getTemid() {
        return temid;
    }

    public void setTemid(String temid) {
        this.temid = temid;
    }
}