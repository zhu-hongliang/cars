package com.accp.domain;

public class Dimission {
    private Integer dinid;

    private Integer sid;

    private String dindate;

    private String dincause;

    public Integer getDinid() {
        return dinid;
    }

    public void setDinid(Integer dinid) {
        this.dinid = dinid;
    }

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public String getDindate() {
        return dindate;
    }

    public void setDindate(String dindate) {
        this.dindate = dindate;
    }

    public String getDincause() {
        return dincause;
    }

    public void setDincause(String dincause) {
        this.dincause = dincause;
    }
}